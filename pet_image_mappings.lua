-- Function to get the image file name for a pet
function get_pet_image(pet_name)
    -- Clean up the pet name (remove spaces and special characters)
    local clean_name = pet_name:gsub("%s+", ""):gsub("[^%w]", "")
    
    -- Try to find an exact match first
    if pet_image_mappings[pet_name] then
        return pet_image_mappings[pet_name]
    end
    
    -- Try to find a partial match
    for name, image in pairs(pet_image_mappings) do
        if name:lower():find(clean_name:lower(), 1, true) then
            return image
        end
    end
    
    -- Return default image if no match found
    return pet_image_mappings.default
end

-- Strict predicate: returns true only if the pet name is an EXACT key in the
-- BST pet table. Used by bsthud as a "BST overlap failsafe" — if a pet's name
-- happens to match a known BST jug pet entry, we use that portrait regardless
-- of the player's main job. The partial-match logic in get_pet_image is too
-- loose for this check (e.g. a wyvern named "Ben" would partial-match
-- "FlowerpotBen"), so this function only honors exact keys.
function has_pet_image(pet_name)
    if pet_name == nil then return false end
    return pet_image_mappings[pet_name] ~= nil
end

return {
    get_pet_image = get_pet_image,
    has_pet_image = has_pet_image,
}